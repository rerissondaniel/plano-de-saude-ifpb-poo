    private void alterarJButtonActionPerformed(java.awt.event.ActionEvent evt) {
    	
    	if(nomeJTextField.getText().equals("")){
        	JOptionPane.showMessageDialog(null, "Informe o nome do procedimento", "Informação", JOptionPane.INFORMATION_MESSAGE);
    	}else if(precoJTextField.getText().equals("")){
    		JOptionPane.showMessageDialog(null, "Informe o preço do procedimento", "Informação", JOptionPane.INFORMATION_MESSAGE);
    	}else{
    		
    		try{
    			p.setNome(nomeJTextField.getText());
    			p.setPreco(Float.parseFloat(precoJTextField.getText()));
    			p.setEspecialidade((Especialidade)jComboBox1.getSelectedItem());
    			p.setTipo(consultaJRadioButton.isSelected() ? "consulta" : "cirurgia");
    			new Alteradora().alterarProcedimento(p);
    			JOptionPane.showMessageDialog(null, "Procedimento alterado com sucesso", "Informação", JOptionPane.INFORMATION_MESSAGE);
    			dispose();
    		}catch (UpdateException uex) {
    			JOptionPane.showMessageDialog(null, uex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    		}catch (NumberFormatException nex) {
				JOptionPane.showMessageDialog(null, "Informe um preço válido", "Informação", JOptionPane.INFORMATION_MESSAGE);
			}
    	}
    }                                              

    private void buscarJButtonActionPerformed(java.awt.event.ActionEvent evt) {                                              
        if(nomeJTextField.getText().equals("")){
        	JOptionPane.showMessageDialog(null, "Informe o nome do procedimento", "Informação", JOptionPane.INFORMATION_MESSAGE);
        }else{
        	
        	try{        		
        		p = new Pesquisadora().pesquisarProcedimento(nomeJTextField.getText());
        		precoJTextField.setText(Float.toString(p.getPreco()));
        		initJList(p.getEspecialidade());
        		if(p.getTipo().equals("cirurgia")) { 
        			cirurgiaJRadioButton.setSelected(true);
        		}else{ 
        			consultaJRadioButton.setSelected(true);
        		} 
        		cirurgiaJRadioButton.setEnabled(false);
        		consultaJRadioButton.setEnabled(false);
        	}catch(SearchException sex){
        		JOptionPane.showMessageDialog(null, sex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        	}
        }
    } 

    public void initJList(Especialidade esp){
		
		ArrayList<Especialidade> esps = new ArrayList<Especialidade>();
		
		try{
			esps = new Pesquisadora().getEspecialidades();
		}catch(SearchException sex){
			JOptionPane.showMessageDialog(null, sex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
		}
		
		DefaultComboBoxModel model = new DefaultComboBoxModel(esps.toArray());
		
		model.setSelectedItem(esp);
		
		jComboBox1.setModel(model);
	}

